package cadastro;

public class Sindicato {
    private int idSindicato;
    private String nomeSindicato;
    private String associacao;
    private String clube;

    public int getIdSindicato() {
        return idSindicato;
    }

    public void setIdSindicato(int idSindicato) {
        this.idSindicato = idSindicato;
    }

    public String getNomeSindicato() {
        return nomeSindicato;
    }

    public void setNomeSindicato(String nomeSindicato) {
        this.nomeSindicato = nomeSindicato;
    }

    public String getAssociacao() {
        return associacao;
    }

    public void setAssociacao(String associacao) {
        this.associacao = associacao;
    }

    public String getClube() {
        return clube;
    }

    public void setClube(String clube) {
        this.clube = clube;
    }
}
