package cadastro;

public class Religiao {
    private int idReligiao;
    private String nome;
    private String tipoCrenca;

    public int getIdReligiao() {
        return idReligiao;
    }

    public void setIdReligiao(int idReligiao) {
        this.idReligiao = idReligiao;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTipoCrenca() {
        return tipoCrenca;
    }

    public void setTipoCrenca(String tipoCrenca) {
        this.tipoCrenca = tipoCrenca;
    }
}
